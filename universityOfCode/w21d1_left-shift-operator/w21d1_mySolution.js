function shiftLeft(num, y) {
  return num * Math.pow(2, y);
}
console.log(shiftLeft(-32, 2));
console.log(shiftLeft(-6, 5));
console.log(shiftLeft(12, 4));
console.log(shiftLeft(46, 6));

